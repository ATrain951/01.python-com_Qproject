def getTotalX(a, b):
    # Write your code here
    max_a, min_b, count = max(a), min(b), 0
    for i in range(1, min_b // max_a + 1):
        if sum(i * max_a % num for num in a) + sum(num % (i * max_a) for num in b) == 0:
            count += 1
    return count


